function aumentarjanela(element) { // usa-se 'element' nos parenteses quando não houver variavel atribuida com div de box especifica;
    element.style.height = "200px" // tambem usa-se 'element' no inicio ao inves do nome da variável ou document.
}

function diminuirjanela(element) {
    element.style.height = "100px" // Janela volta para tamanho original;
    
}

